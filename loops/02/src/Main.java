
public class Main {
    public static void main(String[] args) {


        String metin = "Burak";
        int counter = 0;
        while (counter < 5) {
            System.out.println(metin);
            counter++;
        }
        for(int i = 0; i< 3; i++)
        {
            System.out.print(i + " ");
        }
        //1 den 5 e kadar olan sayıların çarpımı for
        int sum = 1;
        for(int k = 1; k <= 5; k++)
        {
            sum = sum * k;
            System.out.println(sum);
        }







    }
}
